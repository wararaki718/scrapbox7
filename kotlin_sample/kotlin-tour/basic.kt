fun main() {
    var customers = 10
    customers = 8
    println(customers)

    customers = customers + 3
    println(customers)

    customers += 7
    println(customers)

    customers -= 3
    println(customers)

    customers *= 2
    println(customers)

    customers /= 3
    println(customers)

    val a: Int = 1000
    println(a)

    val b: String = "log message"
    println(b)

    val c: Double = 3.14
    println(c)

    val d: Long = 100_000_000_000_000
    println(d)

    val e: Boolean = false
    println(e)

    val f: Char = '\n'
    println(f)

    println("DONE")
}
